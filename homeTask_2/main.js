let temp = 40;

if (temp > 30)
{
    console.log("Вы нужно надеть легкую одежду");
}
else if (temp > 20)
{
    console.log("Вы необходимо надеть теплую одежду");
}
else if (temp < 10)
{
    console.log("Вы необходимо надеть зимнюю одежду");
}
else
{
    console.log("Вы можете сами выбрать одежду которую вы хотите надеть");
}